    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $reglas = [
            'nombre_tipo' => 'required|max:20',
            'des_tipo' => 'required|max:100',
        ];
        $this->validate($request,$reglas);
        
        $t_producto = new TipoProducto;
        $t_producto->nombre_produc = $request->nombre_tipo;
        $t_producto->descripcion_produc = $request->des_tipo;

        if ($t_producto->save()) {
            return back();
        }
    }

